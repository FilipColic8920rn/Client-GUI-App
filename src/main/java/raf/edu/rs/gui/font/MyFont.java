package raf.edu.rs.gui.font;

import java.awt.*;

public class MyFont {
    public static Font getLargeFont() {
        return new Font("serif", Font.PLAIN, 35);
    }

    public static Font getMediumFont() {
        return new Font("serif", Font.PLAIN, 26);
    }

    public static Font getSmallFont() {
        return new Font("serif", Font.PLAIN, 18);
    }
}